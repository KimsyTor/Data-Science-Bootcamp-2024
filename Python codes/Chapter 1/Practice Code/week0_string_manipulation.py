firstname = 'Justin'
lastname = 'Huston'

print(firstname, lastname)

# string concat
fullname = firstname + ' ' + lastname
print(fullname)

# use string concat with print statement
print('Hi my first name is '+firstname+' and my last name is '+lastname)

# using f-string
print(f'Hi my first name is {firstname} and my last name is {lastname}')

# creating long string variable
content = '''
Welcome to my blog post,
please find our informative information below,
feel free to contact us whenever you got question.
'''
print(content)
